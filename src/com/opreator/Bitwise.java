package com.opreator;


public class Bitwise {

	
  public static void main(String[] args) {


	  System.out.println("Bitwise");
	  System.out.println();
	  System.out.println(12 & 15); 
	  System.out.println("12 & 15 = " + Integer.toBinaryString(12));
	  System.out.println();
	  System.out.println(11 | 13 & 12); 
	  System.out.println("11 | 13 & 12 = " + Integer.toBinaryString(15));
	  System.out.println();
	  System.out.println(17 & 7 & 49); 
	  System.out.println("17 & 7 & 49= " + Integer.toBinaryString(1));
	 
	  
	 
}
}
